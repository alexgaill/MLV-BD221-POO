<?php
// Fichier de fonction que l'on utilise en procédural
function addition ($num1, $num2){
    return $num1 + $num2;
}
function soustraction ($num1, $num2){
    return $num1 - $num2;
}
function multiplication ($num1, $num2){
    return $num1 * $num2;
}
function devision ($num1, $num2){
    return $num1 / $num2;
}